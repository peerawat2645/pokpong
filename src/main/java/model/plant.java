package model;

public class plant {
	private String name;
	private String nameSci;
	private int height;
	private String nature;
	private int month;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNameSci() {
		return nameSci;
	}
	public void setNameSci(String nameSci) {
		this.nameSci = nameSci;
	}
	public String getNature() {
		return nature;
	}
	public void setNature(String nature) {
		this.nature = nature;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}

	
}
